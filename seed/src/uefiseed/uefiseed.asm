; uefiseed.asm
;
; A dependency-free x86-64 UEFI loader for FruityOS.  This one NASM source can
; emit either the PE32+ EFI application or a FAT16 removable-media image that
; contains it as EFI/BOOT/BOOTX64.EFI.
;
; PE application:
;   nasm -f bin -DINITRD_JAR='"initrd.jar"' \
;       uefiseed.asm -o BOOTX64.EFI
;
; FAT image:
;   nasm -f bin -DUEFI_IMAGE -DEFI_APP='"BOOTX64.EFI"' \
;       -DEFI_SIZE=123456 uefiseed.asm -o fruityos_uefi.img

%ifdef UEFI_IMAGE

bits 16
org 0

%ifndef EFI_APP
  %error "EFI_APP must name the assembled BOOTX64.EFI file"
%endif
%ifndef EFI_SIZE
  %error "EFI_SIZE must be the BOOTX64.EFI file size in bytes"
%endif

%assign SECTOR_SIZE       512
%assign PARTITION_START   2048        ; 1 MiB alignment
%assign TOTAL_SECTORS     32768       ; 16 MiB FAT16 EFI system partition
%assign RESERVED_SECTORS  1
%assign FAT_COUNT         2
%assign SECTORS_PER_FAT   128
%assign ROOT_ENTRIES      512
%assign ROOT_SECTORS      32
%assign DATA_START        (RESERVED_SECTORS + FAT_COUNT * SECTORS_PER_FAT + ROOT_SECTORS)
%assign EFI_CLUSTERS      ((EFI_SIZE + SECTOR_SIZE - 1) / SECTOR_SIZE)
%assign EFI_FIRST_CLUSTER 4

%macro emit_fat 0
    dw 0xfff8                    ; clusters 0 and 1 are reserved
    dw 0xffff
    dw 0xffff                    ; cluster 2: EFI directory
    dw 0xffff                    ; cluster 3: BOOT directory
  %assign fat_cluster EFI_FIRST_CLUSTER
  %rep EFI_CLUSTERS
    %if fat_cluster = EFI_FIRST_CLUSTER + EFI_CLUSTERS - 1
      dw 0xffff
    %else
      dw fat_cluster + 1
    %endif
    %assign fat_cluster fat_cluster + 1
  %endrep
%endmacro

; Protective legacy MBR with one EFI system partition.  UEFI firmware locates
; the FAT volume through this entry; legacy Seed remains a separate image.
times 446 db 0
db 0                            ; not legacy-BIOS bootable
db 0xfe, 0xff, 0xff             ; starting CHS (LBA is authoritative)
db 0xef                         ; EFI system partition
db 0xfe, 0xff, 0xff             ; ending CHS
dd PARTITION_START
dd TOTAL_SECTORS
times SECTOR_SIZE - 2 - ($ - $$) db 0
dw 0xaa55
times PARTITION_START * SECTOR_SIZE - ($ - $$) db 0

fat_volume:
; BIOS parameter block. Its x86 boot code is intentionally inert.
db 0xeb, 0x3c, 0x90
db 'FRUITYOS'
dw SECTOR_SIZE
db 1                            ; sectors per cluster
dw RESERVED_SECTORS
db FAT_COUNT
dw ROOT_ENTRIES
dw TOTAL_SECTORS
db 0xf8
dw SECTORS_PER_FAT
dw 32                           ; sectors per track (informational)
dw 64                           ; heads (informational)
dd PARTITION_START              ; hidden sectors
dd 0                            ; large sector count
db 0x80
db 0
db 0x29
dd 0x03002026
db 'FRUITYOS   '
db 'FAT16   '
cli
.hang:
hlt
jmp .hang
times SECTOR_SIZE - 2 - ($ - fat_volume) db 0
dw 0xaa55

fat_one:
emit_fat
times SECTORS_PER_FAT * SECTOR_SIZE - ($ - fat_one) db 0

fat_two:
emit_fat
times SECTORS_PER_FAT * SECTOR_SIZE - ($ - fat_two) db 0

; FAT16 root directory: EFI/
root_directory:
db 'FRUITYOS   '
db 0x08
times 20 db 0
db 'EFI        '
db 0x10
times 14 db 0
dw 2
dd 0
times ROOT_SECTORS * SECTOR_SIZE - ($ - root_directory) db 0

; Cluster 2: EFI/BOOT/
efi_directory:
db '.          '
db 0x10
times 14 db 0
dw 2
dd 0
db '..         '
db 0x10
times 14 db 0
dw 0
dd 0
db 'BOOT       '
db 0x10
times 14 db 0
dw 3
dd 0
times SECTOR_SIZE - ($ - efi_directory) db 0

; Cluster 3: EFI/BOOT/BOOTX64.EFI
boot_directory:
db '.          '
db 0x10
times 14 db 0
dw 3
dd 0
db '..         '
db 0x10
times 14 db 0
dw 2
dd 0
db 'BOOTX64 EFI'
db 0x20
times 14 db 0
dw EFI_FIRST_CLUSTER
dd EFI_SIZE
times SECTOR_SIZE - ($ - boot_directory) db 0

incbin EFI_APP
times EFI_CLUSTERS * SECTOR_SIZE - EFI_SIZE db 0
times (PARTITION_START + TOTAL_SECTORS) * SECTOR_SIZE - ($ - $$) db 0

%else

bits 64
org 0
default rel

%ifndef INITRD_JAR
  %error "INITRD_JAR must name the FruityOS initrd archive"
%endif

%define EFI_SUCCESS            0
%define EFI_LOAD_ERROR         0x8000000000000001
%define ALLOCATE_ADDRESS       2
%define EFI_LOADER_DATA        2
%define BOOT_SERVICES_OFFSET   96
%define CON_OUT_OFFSET         64
%define OUTPUT_STRING_OFFSET   8
%define ALLOCATE_PAGES_OFFSET  40
%define GET_MEMORY_MAP_OFFSET  56
%define EXIT_BOOT_OFFSET       232
%define PAGE_SIZE              4096
%define INITRD_ADDRESS         0x2000000
%define IMAGE_BASE             0x1000000

; ---------------------------------------------------------------------------
; DOS and PE32+ headers.  No linker, objcopy, GNU-EFI stub, or external header
; is involved.  The code is position-independent, but a conventional preferred
; base and a real relocation directory are supplied for strict PE/COFF loaders.
; Code and data also have separate permissions for firmware that enforces W^X.
; ---------------------------------------------------------------------------

dos_header:
dw 0x5a4d                      ; MZ
times 0x3a db 0
dd pe_header
times 0x80 - ($ - $$) db 0

pe_header:
dd 0x00004550                  ; PE\0\0
dw 0x8664                      ; AMD64
dw 3                           ; .text, .data, and .reloc
dd 0                           ; timestamp
dd 0, 0                        ; COFF symbols
dw optional_end - optional_header
dw 0x022e                      ; executable, large-address-aware, stripped

optional_header:
dw 0x20b                       ; PE32+
db 0, 2                        ; linker version
dd text_raw_size
dd data_raw_size + reloc_raw_size
dd 0
dd 0x1000 + efi_main - text_start
dd 0x1000
dq IMAGE_BASE                  ; preferred load address; relocatable elsewhere
dd 0x1000                      ; section alignment
dd 0x200                       ; file alignment
dw 0, 0                        ; OS version
dw 0, 0                        ; image version
dw 0, 0                        ; subsystem version
dd 0
dd image_size
dd 0x200                       ; headers size
dd 0                           ; checksum
dw 10                          ; EFI application
dw 0x0140                      ; dynamic base and NX compatible
dq 0x100000, 0x1000            ; stack reserve and commit
dq 0x100000, 0x1000            ; heap reserve and commit
dd 0
dd 16                          ; data-directory count
times 5 * 8 db 0
dd reloc_rva, reloc_virtual_size ; base relocation directory
times 10 * 8 db 0
optional_end:

db '.text', 0, 0, 0
dd text_virtual_size
dd 0x1000
dd text_raw_size
dd 0x200
dd 0, 0
dw 0, 0
dd 0x60000020                  ; code, execute, read

db '.data', 0, 0, 0
dd data_virtual_size
dd data_rva
dd data_raw_size
dd data_raw_offset
dd 0, 0
dw 0, 0
dd 0xc0000040                  ; initialized data, read, write

db '.reloc', 0, 0
dd reloc_virtual_size
dd reloc_rva
dd reloc_raw_size
dd reloc_raw_offset
dd 0, 0
dw 0, 0
dd 0x42000040                  ; initialized data, read, discardable

times 0x200 - ($ - $$) db 0

text_start:

; EFI entry: RCX is the image handle and RDX is EFI_SYSTEM_TABLE.
efi_main:
    push rbp
    mov rbp, rsp
    push rbx
    push rsi
    push rdi
    push r12
    push r13
    push r14
    push r15
    sub rsp, 40                ; shadow space, fifth argument, and alignment

    mov r12, rcx
    mov r13, rdx
    mov r14, [r13 + BOOT_SERVICES_OFFSET]
    mov r15, [r13 + CON_OUT_OFFSET]

    mov al, 'U'
    out 0xe9, al

    lea rdx, [rel loading_message]
    call print

    ; Temporary page tables used during the handoff: identity-map 4 GiB with
    ; 2 MiB pages so the firmware-loaded PE remains executable after CR3 moves.
    mov edx, 0x1000
    mov r8d, 6
    call allocate_at
    test rax, rax
    jnz allocation_failed
    mov al, '1'
    out 0xe9, al

    ; Kernel, ring-0 stack, IDT, and interrupt stacks below 512 KiB.
    mov edx, 0x0f000
    mov r8d, 0x71
    call allocate_at
    test rax, rax
    jnz allocation_failed
    mov al, '2'
    out 0xe9, al

    ; Pulp's permanent page-table arena at 1 MiB.
    mov edx, 0x100000
    mov r8d, 0x103
    call allocate_at
    test rax, rax
    jnz allocation_failed
    mov al, '3'
    out 0xe9, al

    ; Pulp's three fixed-block heaps at 4 MiB.
    mov edx, 0x400000
    mov r8d, 0x3d0
    call allocate_at
    test rax, rax
    jnz allocation_failed
    mov al, '4'
    out 0xe9, al

    ; A stable copy of the initrd, above the kernel heaps and PE image and
    ; below the first task's physical arena.
    mov edx, INITRD_ADDRESS
    mov r8, (initrd_end - initrd_payload + PAGE_SIZE - 1) / PAGE_SIZE
    call allocate_at
    test rax, rax
    jnz allocation_failed
    mov al, '5'
    out 0xe9, al

    ; Copy the complete initrd to its stable post-firmware address.
    lea rsi, [rel initrd_payload]
    mov edi, INITRD_ADDRESS
    mov ecx, initrd_end - initrd_payload
    rep movsb

    ; Find /pulp.sys in the initrd Jar.
    mov esi, INITRD_ADDRESS
.find_pulp:
    cmp byte [rsi], 0
    je pulp_missing
    lea rdi, [rel pulp_name]
    mov ecx, 11
    repe cmpsb
    je .pulp_found
.skip_name:
    lodsb
    test al, al
    jnz .skip_name
    mov rax, [rsi]
    lea rsi, [rsi + rax + 8]
    jmp .find_pulp
.pulp_found:
    add rsi, 8

    ; Expand the Juicer-compressed kernel directly to its linked address.
    mov edi, 0x10000
.unpack:
    lodsb
    cmp al, 255
    jne .literal
    lodsw
    movzx ecx, ax
    movzx edx, ax
    shr edx, 6
    and ecx, 63
    jz .escaped_ff
    dec ecx
    jz .kernel_ready
    inc ecx
    push rsi
    mov rsi, rdi
    sub rsi, rdx
    dec rsi
    rep movsb
    pop rsi
    jmp .unpack
.escaped_ff:
    mov al, 255
.literal:
    stosb
    jmp .unpack

.kernel_ready:
    ; ExitBootServices requires the key from the immediately preceding memory
    ; map.  Retry if firmware changes the key while completing the call.
.exit_retry:
    mov qword [rel memory_map_size], memory_map_end - memory_map
    lea rcx, [rel memory_map_size]
    lea rdx, [rel memory_map]
    lea r8, [rel memory_map_key]
    lea r9, [rel descriptor_size]
    lea rax, [rel descriptor_version]
    mov [rsp + 32], rax
    call qword [r14 + GET_MEMORY_MAP_OFFSET]
    test rax, rax
    jnz map_failed

    mov rcx, r12
    mov rdx, [rel memory_map_key]
    call qword [r14 + EXIT_BOOT_OFFSET]
    test rax, rax
    jnz .exit_retry

    cli
    cld
    mov al, 'E'
    out 0xe9, al
    call make_identity_map
    call set_vga_text_mode

    mov rsp, 0x40000
    mov esi, INITRD_ADDRESS
    mov al, 'H'
    out 0xe9, al
    mov eax, 0x10100
    jmp rax

allocation_failed:
    mov al, 'A'
    out 0xe9, al
    lea rdx, [rel allocation_message]
    jmp report_error
map_failed:
    mov al, 'M'
    out 0xe9, al
    lea rdx, [rel map_message]
    jmp report_error
pulp_missing:
    mov al, 'P'
    out 0xe9, al
    lea rdx, [rel pulp_message]
report_error:
    call print
    mov rax, EFI_LOAD_ERROR
    add rsp, 40
    pop r15
    pop r14
    pop r13
    pop r12
    pop rdi
    pop rsi
    pop rbx
    pop rbp
    ret

; AllocatePages(AllocateAddress, EfiLoaderData, pages, &address).
; Input: RDX = physical address, R8 = page count.
allocate_at:
    mov [rel allocation_address], rdx
    mov ecx, ALLOCATE_ADDRESS
    mov edx, EFI_LOADER_DATA
    lea r9, [rel allocation_address]
    call qword [r14 + ALLOCATE_PAGES_OFFSET]
    ret

; Input: RDX points to a zero-terminated UTF-16 string.
print:
    mov rcx, r15
    call qword [r15 + OUTPUT_STRING_OFFSET]
    ret

; PML4 + PDPT + four page directories at 0x1000..0x6fff.
make_identity_map:
    xor eax, eax
    mov edi, 0x1000
    mov ecx, (6 * PAGE_SIZE) / 8
    rep stosq
    mov qword [abs 0x1000], 0x2003
    mov qword [abs 0x2000], 0x3003
    mov qword [abs 0x2008], 0x4003
    mov qword [abs 0x2010], 0x5003
    mov qword [abs 0x2018], 0x6003
    mov edi, 0x3000
    mov eax, 0x83
    mov ecx, 512 * 4
.page:
    stosq
    add rax, 0x200000
    loop .page
    mov eax, 0x1000
    mov cr3, rax
    ret

; OVMF commonly leaves VGA hardware in a graphics mode.  FruityOS 0.03 writes
; its console to 0xb8000, so restore the standard 80x25 color-text registers.
set_vga_text_mode:
    ; Turn off the Bochs/QEMU VBE extension before touching VGA registers.
    mov dx, 0x1ce
    mov ax, 4
    out dx, ax
    inc dx
    xor ax, ax
    out dx, ax

    lea rsi, [rel vga_registers]
    mov dx, 0x3c2
    lodsb
    out dx, al

    mov dx, 0x3c4
    xor ecx, ecx
.seq:
    mov al, cl
    out dx, al
    inc dx
    lodsb
    out dx, al
    dec dx
    inc ecx
    cmp ecx, 5
    jb .seq

    ; Unlock CRTC registers 0-7 and 16-17.
    mov dx, 0x3d4
    mov al, 3
    out dx, al
    inc dx
    in al, dx
    or al, 0x80
    out dx, al
    dec dx
    mov al, 0x11
    out dx, al
    inc dx
    in al, dx
    and al, 0x7f
    out dx, al
    dec dx

    xor ecx, ecx
.crtc:
    mov al, cl
    out dx, al
    inc dx
    lodsb
    out dx, al
    dec dx
    inc ecx
    cmp ecx, 25
    jb .crtc

    mov dx, 0x3ce
    xor ecx, ecx
.graphics:
    mov al, cl
    out dx, al
    inc dx
    lodsb
    out dx, al
    dec dx
    inc ecx
    cmp ecx, 9
    jb .graphics

    xor ecx, ecx
.attribute:
    mov dx, 0x3da
    in al, dx
    mov dx, 0x3c0
    mov al, cl
    out dx, al
    lodsb
    out dx, al
    inc ecx
    cmp ecx, 21
    jb .attribute
    mov dx, 0x3da
    in al, dx
    mov dx, 0x3c0
    mov al, 0x20
    out dx, al

    call load_vga_font

    ; GOP firmware may leave the VGA DAC palette black. Restore the standard
    ; sixteen text colors used by FruityOS attributes.
    lea rsi, [rel vga_palette]
    mov dx, 0x3c8
    xor al, al
    out dx, al
    inc dx
    mov ecx, 16 * 3
.palette:
    lodsb
    out dx, al
    loop .palette
    ret

; Load the public-domain VGA16 font into VGA plane 2. UEFI GOP implementations
; are not required to leave a legacy text font installed.
load_vga_font:
    push rbx
    mov dx, 0x3c4
    mov ax, 0x0402             ; sequencer map mask: plane 2
    out dx, ax
    mov ax, 0x0604             ; sequential addressing
    out dx, ax
    mov dx, 0x3ce
    mov ax, 0x0204             ; graphics read map: plane 2
    out dx, ax
    mov ax, 0x0005             ; disable odd/even addressing
    out dx, ax
    mov ax, 0x0406             ; map graphics memory at 0xa0000
    out dx, ax

    lea rsi, [rel vga_font]
    mov edi, 0xa0000
    mov ebx, 256
.glyph:
    mov ecx, 16
    rep movsb
    add rdi, 16                ; VGA reserves 32 bytes for each glyph
    dec ebx
    jnz .glyph

    mov dx, 0x3c4
    mov ax, 0x0302             ; text planes 0 and 1
    out dx, ax
    mov ax, 0x0204             ; restore odd/even text addressing
    out dx, ax
    mov dx, 0x3ce
    mov ax, 0x0004
    out dx, ax
    mov ax, 0x1005
    out dx, ax
    mov ax, 0x0e06
    out dx, ax
    pop rbx
    ret

text_end:
text_virtual_size equ text_end - text_start
text_raw_size equ (text_virtual_size + 0x1ff) & ~0x1ff
data_rva equ 0x1000 + ((text_virtual_size + 0xfff) & ~0xfff)
data_raw_offset equ data_rva - (0x1000 - 0x200)
times text_raw_size - text_virtual_size db 0
; Keep every section at the same RVA-to-file-offset delta as .text. NASM's
; flat-binary relative expressions then match the addresses assigned by the
; PE loader even though each virtual section begins on a 4 KiB boundary.
times data_raw_offset - ($ - $$) db 0

